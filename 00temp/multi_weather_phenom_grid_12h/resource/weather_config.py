# 雨夹雪(06)不属于任何互斥组，独立存在

# 快速查询：某电码属于哪个互斥组
def get_exclusive_group(code: str) -> str | None:
    """返回该电码所属的互斥分类组名，若不属于任何组则返回None"""
    for group_name, codes in MUTUALLY_EXCLUSIVE_GROUPS.items():
        if code in codes:
            return group_name
    return None


def is_mutually_exclusive(code_a: str, code_b: str) -> bool:
    """判断两种天气现象是否互斥（属于同一互斥组）"""
    group_a = get_exclusive_group(code_a)
    group_b = get_exclusive_group(code_b)
    if group_a is None or group_b is None:
        return False
    return group_a == group_b


